// دالة لتطبيق اللغة المحددة
function applyLanguage(lang) {
  // حفظ اللغة المختارة في localStorage
  localStorage.setItem('selectedLanguage', lang);
  
  
  // تطبيق الترجمات
  const translation = translations[lang] || translations['en'];
  
  // عناصر الهيدر
  document.querySelectorAll('#all-glasses-header').forEach(el => el.textContent = translation['header.new']);
  document.querySelectorAll('#acces-header').forEach((el, index) => {
      if (index === 0) el.textContent = translation['header.customize'];
      if (index === 1) el.textContent = translation['header.eyeglass'];
      if (index === 2) el.textContent = translation['header.sunglass'];
  });
  document.querySelector('#contact-header').textContent = translation['header.settings'];
  
  // الشرائح
  document.querySelector('.content-1 .p-contant').textContent = translation['slide1.subtitle'];
  document.querySelector('.content-1 .h1-contant').innerHTML = translation['slide1.title'].replace('<br>', '<br>');
  document.querySelector('.content-1 .button-con').textContent = translation['slide1.button'];
  
  document.querySelector('.content-2 .p-contant').textContent = translation['slide2.subtitle'];
  document.querySelector('.content-2 .h1-contant').innerHTML = translation['slide2.title'].replace('<br>', '<br>');
  document.querySelector('.content-2 .button-con').textContent = translation['slide2.button'];
  
  document.querySelector('.content-3 .p-contant').textContent = translation['slide3.subtitle'];
  document.querySelector('.content-3 .h1-contant').innerHTML = translation['slide3.title'].replace('<br>', '<br>');
  document.querySelector('.content-3 .button-con').textContent = translation['slide3.button'];
  
  document.querySelector('.content-4 .p-contant').textContent = translation['slide4.subtitle'];
  document.querySelector('.content-4 .h1-contant').innerHTML = translation['slide4.title'].replace('<br>', '<br>');
  document.querySelector('.content-4 .button-con').textContent = translation['slide4.button'];
  
  // العناوين
  document.querySelectorAll('.h1-new-1').forEach((el, index) => {
      if (index === 0) el.textContent = translation['newProducts'];
      if (index === 1) el.textContent = translation['newBrands'];
      if (index === 2) el.textContent = translation['newAccessories'];
  });
  
  // الأزرار
  document.querySelectorAll('.button-acc').forEach(el => el.textContent = translation['buyNow']);
  document.querySelectorAll('.quick-add-button-css-1').forEach(el => el.textContent = translation['quickAdd']);
  
  // صناديق التصنيفات
  document.querySelector('.box-acc .h1box').textContent = translation['accessoriesTitle'];
  document.querySelectorAll('.box-acc .pbox').forEach((el, index) => {
      if (index === 0) el.textContent = translation['accessoriesDesc'].split(' and ')[0];
      if (index === 1) el.textContent = 'and ' + translation['accessoriesDesc'].split(' and ')[1];
  });
  
  document.querySelector('.box-men .h1box').textContent = translation['menTitle'];
  document.querySelectorAll('.box-men .pbox').forEach((el, index) => {
      if (index === 0) el.textContent = translation['menDesc'].split(' and ')[0];
      if (index === 1) el.textContent = 'and ' + translation['menDesc'].split(' and ')[1];
  });
  
  document.querySelector('.box-women .h1box').textContent = translation['womenTitle'];
  document.querySelectorAll('.box-women .pbox').forEach((el, index) => {
      if (index === 0) el.textContent = translation['womenDesc'].split(' and ')[0];
      if (index === 1) el.textContent = 'and ' + translation['womenDesc'].split(' and ')[1];
  });
  
  document.querySelector('.box-chil .h1box').textContent = translation['kidsTitle'];
  document.querySelectorAll('.box-chil .pbox').forEach((el, index) => {
      if (index === 0) el.textContent = translation['kidsDesc'].split(' and ')[0];
      if (index === 1) el.textContent = 'and ' + translation['kidsDesc'].split(' and ')[1];
  });
  
  document.querySelector('.box-sun .h1box').textContent = translation['sunglassesTitle'];
  document.querySelectorAll('.box-sun .pbox').forEach((el, index) => {
      if (index === 0) el.textContent = translation['sunglassesDesc'].split(' and ')[0];
      if (index === 1) el.textContent = 'and ' + translation['sunglassesDesc'].split(' and ')[1];
  });
  
  // ألوان الموقع
  document.getElementById('webc').textContent = translation['websiteColors'];
}

// عند تحميل الصفحة، تطبيق اللغة المحفوظة
document.addEventListener('DOMContentLoaded', function() {
  const savedLang = localStorage.getItem('selectedLanguage') || 'en';
  applyLanguage(savedLang);
  
  // الاستماع لتغييرات اللغة من صفحة الإعدادات
  window.addEventListener('languageChanged', function(e) {
      applyLanguage(e.detail.lang);
  });
});

export { applyLanguage };